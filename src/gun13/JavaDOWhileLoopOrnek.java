package gun13;

public class JavaDOWhileLoopOrnek {
    public static void main(String[] args) {


        int i=0;
        do{
            System.out.println("numara : "+i++);
        }while (i<=5);

        int a=0;
        do{
            System.out.println("numara = " + a++);
        }while (a>5);
    }
}
