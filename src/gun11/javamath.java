package gun11;

public class javamath {
    public static void main(String[] args) {

        System.out.println("mutlak değer:"+Math.abs(-10));
        System.out.println("max değer:"+Math.max(-10,-11));
        System.out.println("min  değer:"+Math.min(-10,-11));
        System.out.println("ceil değer(3.1):"+Math.ceil(3.1));
        System.out.println("ceil değer(-3.1):"+Math.ceil(-3.1));
        System.out.println("floor değer(3.7):"+Math.floor(3.7));
        System.out.println("floor değer(-3.7):"+Math.floor(3.7));
        System.out.println("round değer(3.4):"+Math.round(3.4));
        System.out.println("round değer(-3.4):"+Math.round(-3.4));
        System.out.println("kuvveti(-3,4):"+Math.pow(-3,4));
        System.out.println("sqrt değer(161):"+Math.sqrt(161));
        System.out.println("random değer():"+Math.random());

    }
}
